/*
                        JS Execution Phases

        
    1. Creation Phase(Memory allocation phase) -

        * variables declared are initialised with undefined.
        * The this keyword is set (varies based on strict mode and environment).
        * Global Execution Context (GEC) is created.
        * Function declarations are stored in memory with their full definitions.
         
    
    2. Execution Phase :-

        * Variable assignments and function calls happen.
        * The JavaScript engine starts executing the code line by line.
        * Expressions are evaluated.

*/